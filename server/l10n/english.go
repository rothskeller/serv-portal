package l10n

// english maps message keys used in the UI into English phrases.  In most of
// the UI, the message keys are the English text, and so are not included here.
// This map contains only those that are unwieldy or that need to have a
// message key that differs from the English text (usually because the same
// English text has different translations in different contexts).
var english = map[string]string{
	"Home[ADDR]": "Home",
	"Home[PAGE]": "Home",
}
