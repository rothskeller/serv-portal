<script>
export default {
  data: () => ({ folder: null }),
  created() {
    this.loadFolder()
  },
  watch: {
    $route: 'loadFolder',
  },
  methods: {
    async loadFolder() {
      this.folder = (await this.$axios.get(`/api/folders/${this.$route.params.id}`)).data
      this.$store.commit('setPage', { title: this.folder.name || 'Files' })
    },
    onMove(folderID) { this.$router.push(`/files/${folderID}`) },
    onOpen(doc) {
      if (doc.name.endsWith('.pdf')) window.open(`/api/folders/${this.folder.id}/${doc.id}`, '_blank')
      else window.location.href = `/api/folders/${this.folder.id}/${doc.id}`
    },
  },
}
</script>

