// FireDistrict returns the number of the fire district containing the address,
// or zero if not available.
func FireDistrict(a *model.Address) int {
	if a.Latitude == 0 || a.Longitude == 0 {
		return 0
	}
	if inRegion(District1, a.Latitude, a.Longitude) {
		return 1
	}
	if inRegion(District2, a.Latitude, a.Longitude) {
		return 2
	}
	if inRegion(District3, a.Latitude, a.Longitude) {
		return 3
	}
	if inRegion(District4, a.Latitude, a.Longitude) {
		return 4
	}
	if inRegion(District5, a.Latitude, a.Longitude) {
		return 5
	}
	if inRegion(District6, a.Latitude, a.Longitude) {
		return 6
	}
	if inRegion(Sunnyvale, a.Latitude, a.Longitude) {
		fmt.Fprintf(os.Stderr, "ERROR: District region issue: (%f,%f) is in the city but not in any region\n", a.Latitude, a.Longitude)
	}
	return 0
}

func inRegion(region orb.Ring, latitude, longitude float64) bool {
	return planar.RingContains(region, orb.Point{longitude, latitude})
}

